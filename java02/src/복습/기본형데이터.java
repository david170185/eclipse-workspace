package 복습;

public class 기본형데이터 {

	public static void main(String[] args) {
		//자동완성 : 컨트롤 + 스페이스바
		// sysout, syso
		System.out.println("자바 두번째 날입니다. ");
		//컨트롤+s
		//컨트롤+F11
		
		//기본데이터 : 정수, 실수, 문자1, 논리형
		//변수(ram 저장공간), 변수명
		//자바에서의 저장공간 생성 시기
		int num = 0; //들어갈 데이터 타입 변수명; 선언!
		//선언을 할 때 자바는 변수가 할당, 4바이트(-21억 ~ 21억)
		
		double eye = 0.0; // 8바이트
		
		char gender = ' '; // 2바이트
		
		boolean food = false; // 1바이트
		
		//변수를 선언할때는 처음값을 넣어주는것이 좋다.
		//처음값 => 초기값, 초기값을 넣어주는 것을 초기화! 라고 한다.
		//변수는 초기화를 해야한다.
		System.out.println(num);
		
		String name = "홍길동";
		//사람이 너무 많이 쓰는 데이터의 형태라서 기본형처럼 사용하게 만들어놓다.
		
		

	}

}
